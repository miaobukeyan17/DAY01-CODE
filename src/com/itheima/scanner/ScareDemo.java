package com.itheima.scanner;

public class ScareDemo {
    /*计算机中进制分类
    十进制:默认
    二进制:0b开头
    八进制:0开头
    十六进制:0x开头
    */

    public static void main(String[] args) {
        System.out.println(110);//十进制
        System.out.println(0b110);//二进制
        System.out.println(0x110);//十六进制
        System.out.println(0110);//八进制
    }
}
